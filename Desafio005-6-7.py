#05-Criar um código que mostre o antecessor e sucessor de um núemro

n = int(input('Digite um número: '))
a = n-1
s = n+1
print('O antecessor de {} é {} e seu sucessor {}'.format(n, a, s))

#06-Code que leia um número e mostre o seu dobro, triplo e raiz quadrada

x = int(input('Digit an number: '))
y = x*2
z = x*3
v = x**(1/2)
print('Your number is {} and your double is {}, triple {} and square root {}'.format(x, y, z, v))

